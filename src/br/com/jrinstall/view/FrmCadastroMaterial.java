     private void montaTabela(List<Material> lista) {
        String colunas[] = new String[]{"Descrição Material", "Valor"};
        ArrayList linhas = new ArrayList<Material>();
        for (Material p : lista) {
            linhas.add(new String[]{p.getDescricaomaterial(), p.getValorPadrao().toString()});
        }
        tableMaterial = new TableMaterial(linhas, colunas, lista);
        jTable.setModel(tableMaterial);
        jTable.getColumnModel().getColumn(0).setHeaderValue("Descrição");
        jTable.getColumnModel().getColumn(0).setPreferredWidth(200);
        jTable.getColumnModel().getColumn(1).setHeaderValue("Valor");
        jTable.getColumnModel().getColumn(1).setPreferredWidth(70);
    }
    private boolean validaCampos() {
        boolean retorno = true;

        StringBuffer str = new StringBuffer();
        str.append("Campos obrigatórios:");

        if (Validacao.campoBranco(jTextDescricao.getText())) {
            retorno = false;
            str.append("\n - Descrição");
            jTextDescricao.requestFocus();
        }

        if (!retorno) {
            MetodosUteis.showMsg(str.toString());
        }
        return retorno;

    }

