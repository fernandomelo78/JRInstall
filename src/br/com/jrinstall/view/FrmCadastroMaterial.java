    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed

        if (jTextPesquisa.getText().length() == 0) {
            MetodosUteis.showMsg("Informe o Item a pesquisar!");

        } else {
            lista = new ArrayList<Material>();
            String criterio = String.valueOf(jComboTipoPesquisa.getSelectedItem().toString());
            Map mapPesquisa = new HashMap();

            if (criterio.equals("Descricao")) {
                mapPesquisa.put("Descricao", "SELECT m FROM Material m WHERE m.descricaomaterial like '" + jTextPesquisa.getText() + "%'");
            }

            lista = new ArrayList<Material>();
            lista = service.getListByHQL((String) mapPesquisa.get(criterio));
            montaTabela(lista);

        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

